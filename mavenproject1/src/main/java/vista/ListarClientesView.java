    // Método para saber qué tipo de búsqueda está seleccionada
    public String getTipoBusqueda() {
        if (buscarRUC.isSelected()) {
            return "RUC";
        } else if (buscarRazonSocial.isSelected()) {
            return "RAZON_SOCIAL";
        }
        return "TODOS"; // Si ninguno está seleccionado
    }
    
